package mawan;

public class Mawan {

    public static void main(String[] args) {
        
       int x = 10; // pengisian nilai
       System.out.println("nilai x :" + x); // output nilai x:10 
       x *= 5;  // perkalian  nilai (x= x * 5)
        System.out.println("setelah perkalian:" + x); // output : setela perkalian :50;
       x /= 5 ; // pembagian nilai (x = x /5)
        System.out.println("setelah pembagian:" + x); // output : setelah pembagian : 10
    }
    
}
